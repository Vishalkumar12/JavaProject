    void display(){
        String ColsName[] =  {"Committee Id","Committee Name","Committee Type","Total Amount","Term Amount","Terms"};
        Object data[][]=null;
        list=new ActionProject().getAllRecords();
        if(list!=null){
            data=new Object[list.size()][ColsName.length];
            int i=0;
            for(BeanComtInfo b:list){
                data[i]=new Object[]{b.getCid(),b.getCommitee_name(),b.getCommitee_type(),b.getTotal_amount(),b.getTerm_amount(),b.getTerms()};
                i++;
            }
            
            DefaultTableModel dm=new DefaultTableModel(data,ColsName){
                public boolean  isCellEditable(int rows, int cols){
                    return false;
                }
            };
            jTable1.setModel(dm);
        } 
        
            else{
            JOptionPane.showMessageDialog(null, "Record not Found...");
        }
    }

