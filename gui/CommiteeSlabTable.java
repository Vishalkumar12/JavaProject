    void dis()
    {
        String ColsName[] =  {"Slab Id","Select Committee","Term No","Amount Given"};
        Object data[][]=null;
        ArrayList<BeanComtSlab> list=new ActionProject().getSlabRecords(cid);
        if(list!=null)
        {
            data=new Object[list.size()][ColsName.length];
            int i=0;
            for(BeanComtSlab b:list)
            {
                data[i]=new Object[]{b.getSlab_id(),b.getSelect_commitee(),b.getTerm_no(),b.getAmount_given()};
                i++;
            }
            
            DefaultTableModel dm=new DefaultTableModel(data,ColsName)
            {
                public boolean  isCellEditable(int rows, int cols)
                {
                    return false;
                }
            };
            jTable2.setModel(dm);
        }
    }
