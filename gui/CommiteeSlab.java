    void display() {
        ArrayList<BeanComtInfo> data = new ActionProject().getAllRecords();
        if (data != null) {
            jComboBox1.removeAllItems();
            for (BeanComtInfo b : data) {
                jComboBox1.addItem(b.getCid() + "," + b.getCommitee_name());
            }
        }
        jComboBox1.insertItemAt("--Please Select--", 0);
        jComboBox1.setSelectedIndex(0);
    }

