    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        BeanComtSlab data = new BeanComtSlab();

        if (jComboBox1.getSelectedIndex() > 0) {
            String select_commitee[] = jComboBox1.getSelectedItem().toString().split(",");
            data.setSelect_commitee(Integer.parseInt(select_commitee[0]));
        }
        //data.setSelect_commitee(select_commitee);
        data.setTerm_no(txtTermNo.getText());
        data.setAmount_given(txtAmtGvn.getText());

        if (new ActionProject().InsertCommiteeSlab(data)) {
            JOptionPane.showMessageDialog(null, "Successfully Submitted...");
        } else {
            JOptionPane.showMessageDialog(null, "Submission Unsuccessful...");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

